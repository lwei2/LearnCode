using namespace std;


int main(void)
{
	cout<<"the size of an integer:\t\t";
	cout<<sizeof(int)<<endl;
	cout<<"the size of a short integer:\t\t";
	cout<<sizeof(short)<<endl;
	cout<<"the size of a long integer:\t\t";
	cout<<sizeof(long)<<endl;
	cout<<"the size of a character:\t\t";
	cout<<sizeof(char)<<endl;
	cout<<"the size of a boolean:\t\t";
	cout<<sizeof(bool)<<endl;
	cout<<"the size of a float:\t\t";
	cout<<sizeof(float)<<endl;
	cout<<"the size of a double:\t\t";
	cout<<sizeof(double)<<endl;
	cout<<"the size of a long long int:\t\t";
	cout<<sizeof(long long int)<<endl;
	return 0;
}
