#include <iostream>

using namespace std;

int main(void)
{
	int i = 8;
	printf("%d\n",++i);//9
	printf("%d\n",--i);//8
	printf("%d\n",i++);//8
	printf("%d\n",-i++);//-8
	printf("%d\n",-i--);//-9
	return 0;
}
